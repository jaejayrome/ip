package pan;

public class Ui {
    /**
     * Creates an Ui instance.
     */
    public Ui() {}

    /**
     * Prints out the Welcome Message to the user.
     */
    public String hello() {
        return "Hello! I'm Pan\nWhat can I do for you?";
    }

    /**
     * Prints out the Farewell Message to the user.
     */
    public String bye() {
        return "Bye. Hope to see you again soon!";
    }
}
